// Format Rupiah
function formatRupiah(number) {
  number = (number + '').replace(/[^0-9\-]/g, '');
  var n = !isFinite(+number) ? 0 : +number;
  var s = n.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ".");
  return "Rp " + s;
}

